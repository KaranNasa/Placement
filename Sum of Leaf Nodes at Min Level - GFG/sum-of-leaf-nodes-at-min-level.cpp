class Solution
{
public:
    int minLeafSum(Node *root)
    {
        //code here
        if(root==NULL)
            return 0;
        
        if(root->left==NULL and root->right==NULL)
            return root->data;
        
        queue<Node*> q;
        q.push(root);
        
        bool check=false;
        while(!q.empty()){
            int size=q.size();
            int curr_sum=0;
            while(size--){
                Node *node=q.front();
                q.pop();
                
                if(node->left==NULL and node->right==NULL){
                    check=true;
                    curr_sum+=node->data;
                }
                
                if(node->left)
                    q.push(node->left);
                if(node->right)
                    q.push(node->right);
            }
            if(check)
                return curr_sum;
        }
        return 0;
    }
    
};

